package user;

public class User {
	private String user_id;
	private int user_password;
	private String user_name;
	private String user_email;
	private String user_college;
	private String user_picture;
	
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public int getUser_password() {
		return user_password;
	}
	public void setUser_password(int user_password) {
		this.user_password = user_password;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public String getUser_college() {
		return user_college;
	}
	public void setUser_college(String user_college) {
		this.user_college = user_college;
	}
	public String getUser_picture() {
		return user_picture;
	}
	public void setUser_picture(String user_picture) {
		this.user_picture = user_picture;
	}
}
